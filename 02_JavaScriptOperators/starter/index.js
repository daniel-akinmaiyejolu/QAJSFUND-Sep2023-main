// Aritmetic Operators
console.log(5 + 5); // prediction = 10
console.log(5 * 10); // prediction = 50
console.log(10 % 3); // prediction = 1
console.log(5 + 10 / 2 * 5 - 10); // prediction = 20
console.log((6 + 10) / 2 * 5 - 10); // prediction = 30

// Assignment Operators
let x = 0
console.log(x = x + 1); // prediction = 1
console.log(x+= 1); // prediction = 2
console.log(x++); // prediction = 2
console.log(++x); // prediction = 4

// Relational Operators
console.log(5 > 3); // prediction = true
console.log(3 != 3); // prediction = false
console.log(3 <= 2 && 5 >2); // prediction = false
console.log(!5>3);// prediction = false

// Mismatched Types
console.log(5 + "5"); // prediction = 55
console.log(5 + true); // prediction = 6
console.log(5 * "5"); // prediction = 25
console.log(1 == true); // prediction = true
console.log(1 === true); // prediction = false



